   SUPPLIERS_ID VARCHAR2(10) NOT NULL,
   FOREIGN KEY(SUPPLIERS_ID) REFERENCES SUPPLIERS(SUPPLIERS_ID),
   FOREIGN KEY(OR_ID) REFERENCES ORDERS(OR_ID),
   FOREIGN KEY(INV_STATUS) REFERENCES inv_status(STATUS_ID)
